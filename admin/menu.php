            <div class="text">
                <p>
                    Hairos Water Park mulai beroperasional tahun 2010 dan merupakan revitalisasi dari Hairos Indah yang telah ada sejak tahun 2005. Berdiri diatas tanah dengan luas ±17 hektar dan terletak di Jl. Jamin Ginting Km 14,5 Pancur Batu Medan, Sumatera Utara atau tepatnya sebelum simpang Tuntungan dari arah kota Medan menuju kearah kota Berastagi, Hairos Water Park adalah merupakan tempat hiburan dan wisata air termewah dan terlengkap yang pernah ada di Kota Medan, Sumatera Utara.
                <p>
                    Hairos Water Park di desain khusus untuk bisa dinikmati seluruh lapisan warga masyarakat dari segala usia, mulai dari anak-anak, remaja, dewasa, bahkan orang tua sekalipun, baik pria maupun wanita. Sejak berkiprah dan berjalan sampai saat ini Hairos Water Park banyak diminati dan sudah banyak dipilih oleh warga masyarakat, instansi, organisasi, dan perusahaan baik dari dalam maupun luar Kota Medan sebagai tempat wisata keluarga, family gathering, corporate gathering, farewell party, retreat, meeting dan sebagainya.
                </p>
                <p>
                    Hairos Water Park akan menawarkan kepada anda sensasi petualangan baru bermain wahana air terlengkap dan terbesar yang pernah ada di Kota Medan. Terdapat banyak wahana-wahana air yang menyenangkan dan bisa membuat anda bergembira bersama dengan keluarga, kerabat ataupun teman-teman anda. Memiliki fasilitas yang lengkap serta didukung dengan kolam renang yang luas akan membuat anda merasa nyaman untuk menikmati setiap wahana air yang ada. Selain itu terdapat juga seluncuran yang cukup menantang dan bisa membuat anda ketagihan untuk selalu mencobanya. Tapi bila anda hanya ingin bersantai sambil menikmati waktu bersama keluarga, anda juga bisa memilih kolam arus yang menenangkan ataupun duduk-duduk santai di joglo-joglo (gazebo) yang telah tersedia di pinggiran kolam.
                </p>
                <p>
                    Apa pun hiburan yang anda inginkan, Hairos Water Park merupakan tempat yang paling cocok untuk mengisi waktu liburan anda sekeluarga. Segera kunjungi Hairos Water Park dan jelajahi setiap wahana yang anda inginkan.</p>
